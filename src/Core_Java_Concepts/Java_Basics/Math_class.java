package Core_Java_Concepts.Java_Basics;

public class Math_class {
    public static void main(String[] args) {
        System.out.println(Math.PI);//PI value
        System.out.println(Math.E);//exponential constant

        double result;
        result=Math.pow(2,3);
        result=Math.abs(-9);
        result=Math.sqrt(100);
        result=Math.round(3.1649);
        result=Math.floor(3.1450);
        System.out.println(result);

    }
}
